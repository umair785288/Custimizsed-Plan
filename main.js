function sendwhatsapp() {
  // Retrieve form data
  var formData = {
      username: document.getElementById('username').value,
      email: document.getElementById('email').value,
      phone: document.getElementById('phone').value,
      address: document.getElementById('address').value,
      city: document.getElementById('city').value,
      nationality: document.getElementById('nationality').value,
      // Use files property to get the selected file
      file: document.getElementById('file').files[0],
      paymentMethod: document.getElementById('paymentMethod').value
  };

  // Replace 'YOUR_WHATSAPP_NUMBER' with the recipient's WhatsApp number
  var whatsappNumber = 'whatsapp:+971568115340';

  // Compose the message
  var message = 'New Form Submission:\n' +
      'Full Name: ' + formData.username + '\n' +
      'Gmail ID: ' + formData.email + '\n' +
      'WhatsApp Number: ' + formData.phone + '\n' +
      'Address: ' + formData.address + '\n' +
      'City: ' + formData.city + '\n' +
      'Nationality: ' + formData.nationality + '\n' +
      'Payment Method: ' + formData.paymentMethod;

  // Create a FormData object and append the file
  var formDataObject = new FormData();
  formDataObject.append('file', formData.file);

  // Open WhatsApp in a new tab with the pre-filled message
  var whatsappLink = 'https://api.whatsapp.com/send?phone=' + whatsappNumber + '&text=' + encodeURIComponent(message);

  // Open WhatsApp link in a new tab
  window.open(whatsappLink, '_blank');
}












